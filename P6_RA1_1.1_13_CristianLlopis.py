# Descripció(programa): Converteix la temperatura de celsius a fahrenheit.

graus = float(input("Introdueix la temperatura en celsius: "))
fahrenheit = (graus * 9/5) + 32
print("La temperatura en fahrenheit és:", fahrenheit)
