# Descripció(programa): Calcula la superfície d’una habitació.

llargada = float(input("Introdueix la llargada (m): "))
amplada = float(input("Introdueix l'amplada (m): "))
superficie = llargada * amplada
print("La superfície de l'habitació és:", superficie, "m²")
